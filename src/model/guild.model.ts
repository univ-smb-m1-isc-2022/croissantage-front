import UserModel from "./user.model";

export default class GuildModel {
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Attributes /////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    readonly id: number = -1;

    readonly guildId: string = '';

    readonly name: string = '';

    readonly icon: string = '';

    readonly members: Set<UserModel> = new Set<UserModel>();

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public constructor(guild?: any) {
        if (!guild) return;
        const { id, guildId, name, icon, members } = guild;
        this.id = id;
        this.guildId = guildId;
        this.name = name;
        this.icon = icon;
        this.members = members;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Methods ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public isNull(): boolean { return this.id === -1; }
}