import UserModel from "./user.model";

export default class GuildModel {
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Attributes /////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    private id: number = -1;

    private guildId: string = '';

    private name: string = '';

    private icon: string = '';

    private members: Set<UserModel> = new Set<UserModel>();

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public constructor(guild?: any) {
        if (!guild) return;
        const {id, guildId, name, icon, members} = guild;
        this.id = id;
        this.guildId = guildId;
        this.name = name;
        this.icon = icon;
        this.members = members;
    }
}